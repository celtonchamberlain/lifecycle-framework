/**
 * affects-lookup.mjs
 *
 * Reverse lookup CLI over the corpus index: given an affects: object id
 * (e.g. repo.governance, data.sales_fact, module.auth) prints every governed
 * document that references it. A ticket id (e.g. ABC-123, matching the project's
 * TICKET_REGEX) is looked up via by_ticket instead.
 *
 * Usage:
 *   node scripts/affects-lookup.mjs <object-id|TICKET-ID> [--json] [--root <dir>]
 *
 * Flags:
 *   --json        Print raw JSON records instead of human-readable output
 *   --root <dir>  Override repo root (testing)
 *
 * Data source: docs/index.json (OUTPUT.INDEX_JSON — generated by
 * build-index.mjs). Stale-cache guard: when any in-scope .md file has a
 * newer mtime than index.json itself (or index.json is missing/unreadable),
 * a warning is printed and the lookup falls back to a live corpus scan via
 * collectMdFiles + parseFrontmatter (_corpus-walk.mjs) so results never
 * trail the working tree.
 *
 * Exit codes:
 *   0  Found at least one entry
 *   1  No entries found for the given id
 *   2  Usage error
 */

import { readFileSync, statSync } from 'fs';
import { join, relative, dirname } from 'path';
import { fileURLToPath } from 'url';
import { collectMdFiles, parseFrontmatter } from './_corpus-walk.mjs';
import { TICKET_REGEX, OUTPUT } from './corpus.config.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// ---------------------------------------------------------------------------
// Argument parsing
// ---------------------------------------------------------------------------

const args = process.argv.slice(2);
const JSON_OUT = args.includes('--json');
const rootIdx = args.indexOf('--root');
const REPO_ROOT = rootIdx !== -1 ? args[rootIdx + 1] : join(__dirname, '..');
const positional = args.filter((a, i) => !a.startsWith('--') && args[i - 1] !== '--root');

function usage() {
  console.error('Usage: node scripts/affects-lookup.mjs <object-id|TICKET-ID> [--json] [--root <dir>]');
}

const KNOWN_FLAGS = new Set(['--json', '--root']);
for (const a of args) {
  if (a.startsWith('--') && !KNOWN_FLAGS.has(a)) {
    console.error(`Unknown flag: ${a}`);
    usage();
    process.exit(2);
  }
}
if (rootIdx !== -1 && (args[rootIdx + 1] === undefined || args[rootIdx + 1].startsWith('--'))) {
  console.error('--root requires a directory argument');
  usage();
  process.exit(2);
}
if (positional.length !== 1) {
  usage();
  process.exit(2);
}
const QUERY = positional[0];

const INDEX_JSON = join(REPO_ROOT, OUTPUT.INDEX_JSON);

// ---------------------------------------------------------------------------
// Date coercion (mirrors build-index.mjs formatDateField, null-preserving for
// JSON output — js-yaml parses unquoted YAML dates as Date objects)
// ---------------------------------------------------------------------------

function formatDate(v) {
  if (v == null) return null;
  if (v instanceof Date) return v.toISOString().split('T')[0];
  return v;
}

// ---------------------------------------------------------------------------
// Stale-cache detection
// ---------------------------------------------------------------------------

/**
 * Returns true when index.json is missing or any in-scope .md source file is
 * newer than it (mtime comparison). The generated INDEX.md is skipped — it is
 * an output artifact written alongside index.json, not a source.
 */
function indexIsStale(indexJsonPath) {
  let indexMtime;
  try { indexMtime = statSync(indexJsonPath).mtimeMs; } catch { return true; }
  for (const f of collectMdFiles(REPO_ROOT, REPO_ROOT)) {
    const rel = relative(REPO_ROOT, f).replace(/\\/g, '/');
    if (rel === OUTPUT.INDEX_MD) continue;
    let mtime;
    try { mtime = statSync(f).mtimeMs; } catch { continue; }
    if (mtime > indexMtime) return true;
  }
  return false;
}

// ---------------------------------------------------------------------------
// Live scan fallback (same record shapes as build-index.mjs buildIndexJson)
// ---------------------------------------------------------------------------

function liveScan() {
  const byObject = {};
  const byTicket = {};

  for (const f of collectMdFiles(REPO_ROOT, REPO_ROOT)) {
    const rel = relative(REPO_ROOT, f).replace(/\\/g, '/');
    if (rel === OUTPUT.INDEX_MD) continue;
    let fm;
    try { fm = parseFrontmatter(readFileSync(f, 'utf8')); } catch { continue; }
    if (!fm || typeof fm !== 'object') continue;

    if (fm.ticket) {
      byTicket[fm.ticket] = {
        title: fm.title || null,
        type: fm.type || null,
        status: fm.status || null,
        path: rel,
        created: formatDate(fm.created),
        updated: formatDate(fm.updated),
        affects: fm.affects || [],
      };
    }

    if (Array.isArray(fm.affects)) {
      for (const obj of fm.affects) {
        if (!obj) continue;
        if (!byObject[obj]) byObject[obj] = [];
        byObject[obj].push({
          ticket: fm.ticket || null,
          task_number: fm.task_number || null,
          title: fm.title || null,
          type: fm.type || null,
          path: rel,
          created: formatDate(fm.created),
        });
      }
    }
  }

  // Same chronological ordering as build-index.mjs
  for (const key of Object.keys(byObject)) {
    byObject[key].sort((a, b) => {
      if (!a.created) return 1;
      if (!b.created) return -1;
      return a.created < b.created ? -1 : 1;
    });
  }

  return { by_object: byObject, by_ticket: byTicket };
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

let index;
if (indexIsStale(INDEX_JSON)) {
  console.warn(`[STALE] ${OUTPUT.INDEX_JSON} is missing or older than the corpus — falling back to a live scan (run \`npm run build:index\` to refresh).`);
  index = liveScan();
} else {
  try {
    index = JSON.parse(readFileSync(INDEX_JSON, 'utf8'));
  } catch (e) {
    console.warn(`[STALE] Cannot read ${OUTPUT.INDEX_JSON} (${e.message}) — falling back to a live scan.`);
    index = liveScan();
  }
}

if (TICKET_REGEX.test(QUERY)) {
  // ticket id → by_ticket lookup
  const rec = (index.by_ticket || {})[QUERY];
  if (!rec) {
    console.error(`No entry found for ticket ${QUERY}.`);
    process.exit(1);
  }
  if (JSON_OUT) {
    console.log(JSON.stringify(rec, null, 2));
  } else {
    console.log(`${QUERY} — ${rec.title || '(untitled)'}`);
    console.log(`  type:    ${rec.type || '—'}`);
    console.log(`  status:  ${rec.status || '—'}`);
    console.log(`  path:    ${rec.path}`);
    console.log(`  created: ${formatDate(rec.created) || '—'}`);
    console.log(`  updated: ${formatDate(rec.updated) || '—'}`);
    const affects = Array.isArray(rec.affects) ? rec.affects : [];
    console.log(`  affects: ${affects.length > 0 ? affects.join(', ') : '—'}`);
  }
  process.exit(0);
}

// object id → by_object lookup
const hits = (index.by_object || {})[QUERY];
if (!hits || hits.length === 0) {
  console.error(`No entries found for object "${QUERY}".`);
  process.exit(1);
}
if (JSON_OUT) {
  console.log(JSON.stringify(hits, null, 2));
} else {
  console.log(`${QUERY} — ${hits.length} reference(s)`);
  for (const h of hits) {
    console.log(`  ${h.ticket || '—'}  ${formatDate(h.created) || '—'}  ${h.path}`);
  }
}
process.exit(0);
